package com.example.teamliquid.pridemaker.entity.legislator.topcontributors;

public class LegislatorTopContributorsAttributes {
	private String cand_name;
    private String cid;
    private String cycle;
    private String origin;
    private String source;
    private String notice;
    
	public LegislatorTopContributorsAttributes() {
		super();
	}

	public String getCand_name() {
		return cand_name;
	}

	public void setCand_name(String cand_name) {
		this.cand_name = cand_name;
	}

	public String getCid() {
		return cid;
	}

	public void setCid(String cid) {
		this.cid = cid;
	}

	public String getCycle() {
		return cycle;
	}

	public void setCycle(String cycle) {
		this.cycle = cycle;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getNotice() {
		return notice;
	}

	public void setNotice(String notice) {
		this.notice = notice;
	}
    
    
    

}
